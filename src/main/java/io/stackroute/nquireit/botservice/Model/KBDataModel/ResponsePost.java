package io.stackroute.nquireit.botservice.Model.KBDataModel;

public class ResponsePost {
    private String id;
    private String Response;

    public ResponsePost() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getResponse() {
        return Response;
    }

    public void setResponse(String response) {
        Response = response;
    }
}
